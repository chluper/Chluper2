package chluper2.simulation;

/**
 * Interfejs sluchacza symulacji
 * @author damian
 */
public interface SimulatorListener {

	/**
	 * Metoda wywolywana kiedy zostanie wykonany kolejny krok
	 */
	public void nextStep();

	/**
	 * Metoda wywolywana kiedy pojawi sie blad symulacji
	 * @param t
	 */
	public void simulationError(Throwable t);
}
